#Pdir un numero al usuario y mostrar el nombre del dia al corresponde(1=lunes)

var1=int(input("ingrese un numero que este entre 1 y 7 para indicar que dia es: "))

if (var1==1):
    
    print("lunes")
 
elif (var1==2):
    
    print("martes")

elif (var1==3): 
    
    print("miercoles")

elif (var1==4):

    print("jueves")

elif (var1==5):

   print("viernes")

elif (var1==6):
   
    print("sabado")

elif (var1==7):
    
    print("domingo")


else: 
    print("no aplica")

